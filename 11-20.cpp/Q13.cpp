#include <iostream>
using namespace std;
int main(){

    cout<<"--logical operators demonstration--"<<endl;
    bool p = true;
    bool q = false;
    cout<<"p = "<<p<<", q = "<<q<<endl;
    cout<<"p && q = "<<(p && q)<<endl; //logical AND
    cout<<"p || q = "<<(p || q)<<endl; //logical OR
    cout<<"!p = "<<(!p)<<endl;         //logical NOT
    cout<<"!q = "<<(!q)<<endl;         //logical NOT
    return 0;
}